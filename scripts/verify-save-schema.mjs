import { readFileSync } from "node:fs";

const worldSource = readFileSync("src/simulation/world.ts", "utf8");
const saveSource = readFileSync("src/wargus/saveGame.ts", "utf8");

const worldStateBody = worldSource.match(/export interface WorldState \{([\s\S]*?)\n\}/)?.[1] ?? "";
const worldFields = [...worldStateBody.matchAll(/^\s{2}([A-Za-z0-9_]+):/gm)].map((match) => match[1]);
const runtimeOnlyFields = new Set([
  "map",
  "unitDefinitions",
  "upgradeDefinitions",
  "missileDefinitions",
  "animationDefinitions",
  "spellDefinitions",
  "allowRules",
  "dependencyRules",
  "buttonDefinitions",
  "aiDefinitions",
  "unitDatabase",
  "tilesetTerrain",
  "engineSettings",
  "events"
]);
const persistedFields = worldFields.filter((field) => !runtimeOnlyFields.has(field));

const savedGameWorldBody = saveSource.match(/interface SavedGame \{[\s\S]*?world: \{([\s\S]*?)\n  \};/)?.[1] ?? "";
const savedGameFields = new Set([...savedGameWorldBody.matchAll(/^\s{4}([A-Za-z0-9_]+)\??:/gm)].map((match) => match[1]));

const createSavedGameBody = saveSource.match(/function createSavedGame[\s\S]*?world: \{([\s\S]*?)\n    \}/)?.[1] ?? "";
const createdFields = new Set([...createSavedGameBody.matchAll(/^\s{6}([A-Za-z0-9_]+):/gm)].map((match) => match[1]));

const loadBody = saveSource.match(/function loadSavedGameFromRaw[\s\S]*?return \{ world, map, camera:/)?.[0] ?? "";
const loadedFields = new Set([...loadBody.matchAll(/\bworld\.([A-Za-z0-9_]+)\s=/g)].map((match) => match[1]));
const loadInitialFields = new Set(["tileSize"]);

const errors = [];
for (const field of persistedFields) {
  if (!savedGameFields.has(field)) {
    errors.push(`SavedGame.world is missing ${field}`);
  }
  if (!createdFields.has(field)) {
    errors.push(`createSavedGame is missing ${field}`);
  }
  if (!loadedFields.has(field) && !loadInitialFields.has(field)) {
    errors.push(`loadSavedGameFromRaw is missing ${field}`);
  }
}

if (errors.length > 0) {
  for (const error of errors) {
    console.error(error);
  }
  console.error(`Save schema errors: ${errors.length}`);
  process.exit(1);
}

